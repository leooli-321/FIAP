	// Visibilidade, tipo de dado e variÃ¡vel
	
	private String nome;
	private int idade;
	private String email;
	private Endereco endereco;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
