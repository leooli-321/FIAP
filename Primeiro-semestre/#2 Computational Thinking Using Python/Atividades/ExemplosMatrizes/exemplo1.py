# Criação de uma matriz 3x3 (quadrada) de números inteiros
matriz = [[5, 7, 8], [4, 3, 2], [2, 8, 4]]

# Exibir a segunda linha da matriz

print(matriz[0], matriz[1], matriz[2])

# Exibir o segundo elemento da terceira linha
print(matriz[2][1])

# Exibir o terceito elemento da segunda linha

print(matriz[1][2])
