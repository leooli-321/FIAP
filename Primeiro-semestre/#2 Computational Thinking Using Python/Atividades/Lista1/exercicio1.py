# Calculando soma, subtração e multiplicação:

num1 = int(input("Informe o primeiro número inteiro: "))
num2 = int(input("Informe o segundo número inteiro: "))

soma = num1 + num2
sub = num1 - num2
mult = num1 * num2

print(f"A soma dos 2 números é {soma} ")
print(f"A subtração dos 2 números é {sub} ")
print(f"A multiplicação dos 2 números é {mult} ")