# Verificar e mostrar se um número inteiro é: maior, menor ou igual a zero (neutro)

num_x = int(input("Digite um número inteiro: "))

if num_x > 0:
    print("O número é positivo")
elif (num_x < 0):
    print("O número é negativo")
else:
    print("O número é neutro")
