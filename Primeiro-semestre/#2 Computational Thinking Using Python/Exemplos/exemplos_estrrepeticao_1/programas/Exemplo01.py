# Exemplo 1: Exibir todos os números inteiros de 1 até 100

for cont in range(1,11): #1 significa o início e 11 significa o fim-1.
    print(cont)


#exemplo 2: Exibir todos os números inteiros de 0 até 19

for i in range(20): #início não definido, então o "range" irá iniciar por 0
    print(i)

# Exemplo 3: Exibir todos os números inteiros de 2 até 20, de 2 em 2 (passo 2)

for j in range(2,22,2): #O último argumento representa um incremento na variável. (inicio, fim, passo)
    print(j)
    