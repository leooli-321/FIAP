# Criação de uma lista com 5 notas de alunos:

listaNotas = [5.5, 7.5, 10.0, 8.3, 9.4]


# Exibir a lista completa

print(listaNotas)

# Exibir o tercdeiro elemento da lista:

print(listaNotas[2])
print(f"Terceiro elemento da lista")

# Exibir o ultimo elemento da lista:

print(f"Ultimo elemento da lista: {listaNotas[-1]}")

# Exibir do primeiro ao terceiro elemento da lista:

print(f"Primeiro ao terceiro elemento da lista: {listaNotas[0:3]}")

