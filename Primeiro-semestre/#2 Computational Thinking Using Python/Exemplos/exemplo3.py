# Função para entrada de dados no Python:

num = input("Digite um número inteiro: ")
print(num)
print(type(num))

# Conversão de string (texto) para número inteiro

num = int(num)
print(type(num))

# Forma mais simplificada de converter o conteúdo do input:
numInteiro = int(input("Digite um número inteiro: "))
print(type(numInteiro))

notaAluno = float(input("Digite a nota do aluno: "))
print(type(notaAluno))